#include <iostream>
using namespace std;

int main() {
    // Khai báo và khởi tạo hai biến lưu số
    int num1 = 50;
    int num2 = 100;

    // Tính tổng của hai số và lưu vào biến total
    int total = num1 + num2;

    // In ra tổng
    cout << "Tong cua hai so " << num1 << " va " << num2 << " la: " << total << endl;

    return 0;
}
