#include <iostream>
using namespace std;

int main()
{
    int num = 4;
    cout << num << endl;
    cout << num++ << endl;
    cout << num << endl;
    cout << ++num <<endl;
    cout << endl;
    cout << num << endl;
    cout << num-- << endl;
    cout << num << endl;
    cout << --num <<endl;
    return 0;
}