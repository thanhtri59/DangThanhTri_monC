#include <iostream>
using namespace std;

int main()
{
    int num = 4;
    // Hiển thị giá trị bằng num.
    cout << "The variable num is " << num << endl;
    cout << "I will now increment num. \n\n";
    // Sử dụng hậu tố ++ để tăng num.
    num++;
    cout << "Now the variable num is " << num << endl;
    cout << "I will increment num again. \n\n";
    // Sử dụng tiền tố ++ để tăng num.
    ++num;
    cout << "Now the variable num is " << num << endl;
    cout << "I will increment num again. \n\n";
    // Sử dụng hậu tố −− để giảm num.
    num--;
    cout << "Now the variable num is " << num << endl;
    cout << "I will increment num again. \n\n";
    // Use prefix −− to increment num.
    --num;
    cout << "Now the variable num is " << num << endl;
    return 0;

}